    MATAIKHOAN NUMBER NOT NULL,
    LOAIGIAODICH NVARCHAR2(100) NOT NULL,
    SOTIEN NUMBER(19,0) NOT NULL,
    NGAYGIAODICH TIMESTAMP NOT NULL,
    MAHOADONBAN NUMBER,
    MAHOADONMUA NUMBER,
    MAPHIEULUONG NUMBER,
    MAQUYETTOAN NUMBER,
