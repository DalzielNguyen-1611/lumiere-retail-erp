CREATE TABLE TAI_KHOAN (
    MATAIKHOAN NUMBER GENERATED AS IDENTITY PRIMARY KEY,
    MACUAHANG NUMBER,
    TENTAIKHOAN NVARCHAR2(255) NOT NULL,
    LOAITAIKHOAN NVARCHAR2(50) NOT NULL,
    SOTAIKHOAN NVARCHAR2(50),
    TENNGANHANG NVARCHAR2(255),
    SODUHIENTAI NUMBER(19,0) NOT NULL,
    MUCDICH NVARCHAR2(100) NOT NULL
);

