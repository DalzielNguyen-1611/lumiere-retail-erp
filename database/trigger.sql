-- 25. TRG_SYNC_STOCK_PURCHASE
CREATE OR REPLACE TRIGGER TRG_SYNC_STOCK_PURCHASE 
AFTER INSERT ON CHI_TIET_PHIEU 
FOR EACH ROW 
DECLARE
    v_loaiphieu NVARCHAR2(100);
    v_khoden NUMBER;
BEGIN 
    SELECT LOAIPHIEU, KHODEN INTO v_loaiphieu, v_khoden 
    FROM PHIEU_KHO WHERE SOPHIEU = :NEW.SOPHIEU;
    
    IF UPPER(v_loaiphieu) LIKE '%NHẬP%' AND v_khoden IS NOT NULL THEN
        MERGE INTO TON_KHO TK 
        USING (SELECT :NEW.MASANPHAM AS SP, v_khoden AS KHO FROM DUAL) src
        ON (TK.MASANPHAM = src.SP AND TK.MAKHO = src.KHO)
        WHEN MATCHED THEN 
            UPDATE SET SOLUONGTON = SOLUONGTON + :NEW.SOLUONG
        WHEN NOT MATCHED THEN 
            INSERT (MASANPHAM, MAKHO, SOLUONGTON, NGAYCAPNHAP) 
            VALUES (:NEW.MASANPHAM, v_khoden, :NEW.SOLUONG, SYSTIMESTAMP);
    END IF;
END; 
/

