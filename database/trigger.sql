-- 20. TRG_SYNC_STOCK_RETURN
CREATE OR REPLACE TRIGGER TRG_SYNC_STOCK_RETURN
AFTER INSERT ON CHI_TIET_DOI_TRA
FOR EACH ROW
DECLARE
    v_makho NUMBER;
BEGIN
    SELECT K.MAKHO INTO v_makho 
    FROM PHIEU_DOI_TRA P 
    JOIN KHO K ON P.MACUAHANG = K.MACUAHANG 
    WHERE P.MAPHIEU = :NEW.MAPHIEU 
    AND ROWNUM = 1;
    UPDATE TON_KHO SET SOLUONGTON = SOLUONGTON + :NEW.SOLUONG_TRA 
    WHERE MASANPHAM = :NEW.MASANPHAM_TRA AND MAKHO = v_makho;
    IF :NEW.MASANPHAM_DOI IS NOT NULL THEN
        UPDATE TON_KHO SET SOLUONGTON = SOLUONGTON - :NEW.SOLUONG_DOI 
        WHERE MASANPHAM = :NEW.MASANPHAM_DOI AND MAKHO = v_makho;
    END IF;
END;
