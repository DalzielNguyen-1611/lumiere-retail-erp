BEFORE DELETE ON DANH_MUC
FOR EACH ROW
DECLARE 
    v_c NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_c FROM SAN_PHAM_DANH_MUC WHERE MADANHMUC = :OLD.MADANHMUC;
    IF v_c > 0 THEN 
        RAISE_APPLICATION_ERROR(-20023, 'Danh mục đang có sản phẩm, không thể xóa!'); 
    END IF;
END;
/

-- 46. TRG_CHECK_GIA_BAN_THAP_HON_NHAP
CREATE OR REPLACE TRIGGER TRG_CHECK_GIA_BAN_THAP_HON_NHAP
BEFORE UPDATE OF GIANIEMYET ON SAN_PHAM
FOR EACH ROW
DECLARE 
    v_gianhap NUMBER;
BEGIN
    SELECT NVL(AVG(DONGIA),0) INTO v_gianhap 
    FROM CHI_TIET_HOA_DON_MUA_HANG 
    WHERE MASANPHAM = :NEW.MASANPHAM;
    
    IF :NEW.GIANIEMYET < v_gianhap AND v_gianhap > 0 THEN
        RAISE_APPLICATION_ERROR(-20024, 'Cập nhật giá bán thấp hơn giá vốn!');
    END IF;
END;
/

