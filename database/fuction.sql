-- 5. FUNC_CHECK_QUYEN_NHAN_VIEN
CREATE OR REPLACE FUNCTION FUNC_CHECK_QUYEN_NHAN_VIEN (
    p_manhanvien NUMBER, 
    p_quyenhan NVARCHAR2
) RETURN NUMBER IS
    v_count NUMBER;
BEGIN 
    SELECT COUNT(*) 
    INTO v_count 
    FROM PHAN_QUYEN_NHAN_VIEN PQ 
    JOIN VAI_TRO VT ON PQ.MAVAITRO = VT.MAVAITRO 
