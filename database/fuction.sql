-- 13. FUNC_CALC_TIEN_HOAN_TRA
CREATE OR REPLACE FUNCTION FUNC_CALC_TIEN_HOAN_TRA (
    p_maphieu NUMBER
) RETURN NUMBER IS
    v_kq NUMBER;
BEGIN 
    SELECT NVL(SUM(SOLUONG_TRA * DONGIA_TRA) - SUM(NVL(SOLUONG_DOI, 0) * NVL(DONGIA_DOI, 0)), 0) 
    INTO v_kq 
    FROM CHI_TIET_DOI_TRA 
    WHERE MAPHIEU = p_maphieu; 
    
    IF v_kq > 0 THEN 
        RETURN v_kq; 
    ELSE 
        RETURN 0; 
    END IF;
END; 
/

